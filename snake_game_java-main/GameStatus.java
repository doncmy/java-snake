/**
 * 游戏状态枚举，表示游戏当前所处的不同状态
 * 
 * 该枚举定义了游戏可能处于的四种基本状态，
 * 用于控制游戏流程和界面渲染逻辑
 */
public enum GameStatus {
    /**
     * 游戏未开始状态 - 显示开始界面，等待玩家按键开始
     */
    NOT_STARTED,
    
    
    /**
     * 游戏运行状态 - 游戏正常进行中，处理玩家输入和游戏逻辑
     */
    RUNNING,
    
    /**
     * 游戏暂停状态 - 游戏暂时停止，保留当前游戏数据
     */
    PAUSED,
    
    /**
     * 游戏结束状态 - 玩家失败或游戏终止，显示结束界面
     */
    GAME_OVER,
    
    /**
     * 模式选择状态 - 显示模式选择界面，等待玩家选择
     */
    MODE_SELECT,
    
    /**
     * 死亡动画状态 - 播放死亡粒子动画后再进入GAME_OVER
     */
    DEATH_ANIMATION
}